/**
 * 组织/人名的名称纯函数工具 —— 不注入任何东西,可被 service 与其它模块直接引用。
 *
 * 为什么在 organization 模块:「党组织名 → 对口行政机构名」是组织域的知识,
 * 原先只存在于 assessment/assess-relations.ts 的私有副本里(考核区域索引用)。
 * 证书发证「按党组织匹配单位」需要同一套规则 —— 与其复制第二份(两份必然漂移),
 * 不如下沉到组织域,assessment 与 certificate 共用同一个定义。
 */

/**
 * 去掉党组织名的后缀,得到对口行政机构的候选名。
 *   「西北分公司党委」→「西北分公司」
 *   「酒泉配送中心党支部」→「酒泉配送中心」
 *   「XX总支部委员会」→「XX」
 *
 * 注意顺序:长后缀在前,否则「总支部委员会」会被「委员会」先吃掉、留下「总支部」。
 * 正则原样取自 assess-relations.ts(2026-06-13 考核 P1.4 起在真实库上验证过),
 * 下沉时一字未改 —— 改它会同时影响考核的「考核关系」推导。
 */
export function stripPartySuffix(name: string): string {
  return name
    .replace(/(总支部委员会|支部委员会|委员会|党总支|党支部|党委|总支|支部)$/u, '')
    .trim();
}

/**
 * 名称归一化 —— 只用于「按名称比对」(组织名 / 人名皆可),
 * **不用于展示,更不用于落库**(落库要留原文快照)。
 *
 * 做两件事:
 *  1. 全角 ASCII → 半角(公文里「(」「)」「Ａ」混用)
 *  2. 去掉**全部**空白
 *
 * ★ 为什么是「去掉全部空白」而不是「折叠成一个空格」:
 *   公文排版会给 2 字姓名中间垫空格来和 3 字姓名对齐 —— 真实样例
 *   「聂  伟」「黑  瑞」「王  鑫」,而库里存的是「聂伟」。折叠成一个空格
 *   仍然匹配不上,必须全部去掉。
 *   中文人名/机构名内部本就无空格,故此法在本域安全;
 *   少数民族姓名用间隔号「·」分隔(「买买提·艾力」)而非空格,不受影响。
 *
 * 注:JS 正则的 \s 已经涵盖全角空格 U+3000(及 U+00A0 等 Unicode 空白),
 * 故无需再写全角空格字面量 —— 写字面量还会触发 eslint no-irregular-whitespace。
 */
export function normalizeName(name: string): string {
  return name
    .replace(/[！-～]/g, (c) => String.fromCharCode(c.charCodeAt(0) - 0xfee0))
    .replace(/\s+/g, '');
}
